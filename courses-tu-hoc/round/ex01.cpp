#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << round(2.3) << endl;   // 2
    cout << round(2.5) << endl;   // 3
    cout << round(2.8) << endl;   // 3
    cout << round(-2.3) << endl;  // -2
    cout << round(-2.5) << endl;  // -3
    cout << round(-2.8) << endl;  // -3
}
